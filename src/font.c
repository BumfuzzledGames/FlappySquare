#include "font.h"

extern unsigned char font_bmp[];
extern unsigned int font_bmp_len;
extern SDL_Renderer *renderer;

static SDL_Texture *font;

int load_font(void) {
    // Load the font texture, it is unprocessed and will
    // need some attention
    SDL_Surface *surface = SDL_LoadBMP_RW(
        SDL_RWFromConstMem(font_bmp, (int)font_bmp_len),
        1);
    if(surface == NULL) {
        SDL_Log("SDLCreateRGBSurfaceWithFromFormat %s", SDL_GetError());
        return 0;
    }

    // The font should be in black and white indexed mode,
    // to make any black pixel transparent
    for(int i = 0; i < surface->format->palette->ncolors; i++) {
        if(surface->format->palette->colors[i].r +
            surface->format->palette->colors[i].g +
            surface->format->palette->colors[i].b == 0)
        {
            surface->format->palette->colors[i].a = 0;
        }
    }

    // Create a new surface that will serve a few purposes:
    // - Convert the font from an indexed mode into 32-bit
    // - Add spacing between the letters, the font will be
    //   eventually rendered with texture filtering on, and
    //   the edges of characters pick up a tiny bit of
    //   neighboring characters if there is no spacing.
    SDL_Surface *surface32 = SDL_CreateRGBSurfaceWithFormat(
        0,
        surface->w + FONT_CHARACTERS_PER_ROW + 1,
        surface->h + FONT_CHARACTER_ROWS + 1,
        32,
        SDL_PIXELFORMAT_RGBA32);
    
    // Blit the characters one by one onto surface32 with
    // spacing
    for(int y = 0; y < FONT_CHARACTER_ROWS; y++) {
        for(int x = 0; x < FONT_CHARACTERS_PER_ROW; x++) {
            SDL_BlitSurface(
                surface,
                &(SDL_Rect){
                    .x = FONT_CHARACTER_WIDTH * x,
                    .y = FONT_CHARACTER_HEIGHT * y,
                    .w = FONT_CHARACTER_WIDTH,
                    .h = FONT_CHARACTER_HEIGHT, },
                surface32,
                &(SDL_Rect){
                    .x = (FONT_CHARACTER_WIDTH + 1) * x + 1,
                    .y = (FONT_CHARACTER_HEIGHT + 1) * y + 1,
                    .w = FONT_CHARACTER_WIDTH,
                    .h = FONT_CHARACTER_HEIGHT, }
            );
        }
    }

    // Finally, scale the surface up. When rendered from
    // a texture with filtering on, this allows you to have
    // relatively sharp pixels at any scale
    SDL_Surface *scaled_surface = SDL_CreateRGBSurfaceWithFormat(
        0,
        surface32->w * FONT_TEXTURE_SCALE,
        surface32->h * FONT_TEXTURE_SCALE,
        32,
        SDL_PIXELFORMAT_RGBA32);
    if(scaled_surface == NULL) {
        SDL_Log("SDL_CreateRGBSurfaceWithFormat %s", SDL_GetError());
        return 0;
    }
    SDL_BlitScaled(surface32, NULL, scaled_surface, NULL);

    // Set texture filtering on
    const char *old_quality = SDL_GetHint(SDL_HINT_RENDER_SCALE_QUALITY);
    SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "linear");

    // Create the font texture from scaled_surface
    font = SDL_CreateTextureFromSurface(renderer, scaled_surface);
    if(font == NULL) {
        SDL_Log("SDL_CreateTextureFromSurface %s", SDL_GetError());
        return 0;
    }

    // Restore texture filtering hint
    SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, old_quality);

    // Set the blend mode to transparent text can be rendered
    SDL_SetTextureBlendMode(font, SDL_BLENDMODE_BLEND);

    // Free surfaces
    SDL_FreeSurface(scaled_surface);
    SDL_FreeSurface(surface32);
    SDL_FreeSurface(surface);
    return 1;
}

void draw_character(int x, int y, char c) {
    float scale;
    SDL_RenderGetScale(renderer, &scale, 0);
    SDL_RenderSetScale(renderer, 1, 1);

    char *char_in_set = SDL_strchr(FONT_CHARSET, c);
    if(char_in_set == NULL)
        char_in_set = SDL_strchr(FONT_CHARSET, '?');
    int charset_index = (int)(char_in_set - FONT_CHARSET);
    int charset_x = charset_index % FONT_CHARACTERS_PER_ROW;
    int charset_y = charset_index / FONT_CHARACTERS_PER_ROW;
    int texture_x =
        FONT_TEXTURE_SCALE +
        (FONT_CHARACTER_WIDTH + 1) * FONT_TEXTURE_SCALE * charset_x;
    int texture_y =
        FONT_TEXTURE_SCALE +
        (FONT_CHARACTER_HEIGHT + 1) * FONT_TEXTURE_SCALE * charset_y;

    SDL_RenderCopyF(
        renderer,
        font,
        &(SDL_Rect) {
            .x = texture_x,
            .y = texture_y,
            .w = FONT_CHARACTER_WIDTH * FONT_TEXTURE_SCALE,
            .h = FONT_CHARACTER_HEIGHT * FONT_TEXTURE_SCALE, },
        &(SDL_FRect) {
            .x = x * scale,
            .y = y * scale,
            .w = FONT_CHARACTER_WIDTH * FONT_DISPLAY_SCALE * scale,
            .h = FONT_CHARACTER_HEIGHT * FONT_DISPLAY_SCALE * scale, } );
    
    SDL_RenderSetScale(renderer, scale, scale);
}

void draw_string(
    int x, int y,
    const SDL_Color *color,
    const char *format, ...)
{
    // Set color and transparency 
    SDL_SetTextureColorMod(font, color->r, color->g, color->b);
    SDL_SetTextureAlphaMod(font, color->a);

    // Format the string
    static char buffer[128];
    va_list arguments;
    va_start(arguments, format);
    if(SDL_vsnprintf(buffer, sizeof(buffer), format, arguments) < 0) {
        SDL_Log("draw_string SDL_vsnprintf failed");
        return;
    }

    // Draw the string character by character
    float start_x = x;
    for(int c = 0; buffer[c]; c++) {
        // Newlines move down one character and return to
        // the starting x coordinate
        if(buffer[c] == '\n') {
            x = start_x;
            y += FONT_CHARACTER_HEIGHT * FONT_DISPLAY_SCALE;
        } else {
            draw_character(x, y, buffer[c]);
            x += FONT_CHARACTER_WIDTH * FONT_DISPLAY_SCALE;
        }
    }
    va_end(arguments);
}

// Get the length of the first line of the string, or
// the length of the string if the string has no newlines
float line_width(const char *string) {
    int width = 0;
    for (; string[width] && string[width]!='\n'; width++) {}
    return width * FONT_CHARACTER_WIDTH * FONT_DISPLAY_SCALE;
}

void draw_string_centered(
    int minx, int maxx,
    int y,
    const SDL_Color *color,
    const char *format, ...) 
{
    // Set color and transparency 
    SDL_SetTextureColorMod(font, color->r, color->g, color->b);
    SDL_SetTextureAlphaMod(font, color->a);

    // Format the string
    static char buffer[128];
    va_list arguments;
    va_start(arguments, format);
    if(SDL_vsnprintf(buffer, sizeof(buffer), format, arguments) < 0) {
        SDL_Log("draw_string SDL_vsnprintf failed");
        return;
    }

    // Draw the string character by character
    float x = minx + (maxx - minx - line_width(buffer)) / 2;
    for(int c = 0; buffer[c]; c++) {
        // Newlines move down one line, and then recalculate
        // the starting x position
        if(buffer[c] == '\n') {
            x = minx + (maxx - minx - line_width(&buffer[c+1])) / 2;
            y += FONT_CHARACTER_HEIGHT * FONT_DISPLAY_SCALE;
        } else {
            draw_character(x, y, buffer[c]);
            x += FONT_CHARACTER_WIDTH * FONT_DISPLAY_SCALE;
        }
    }
}

